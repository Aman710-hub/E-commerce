import React from "react";
import styled from "styled-components";
import { useFilterContext } from "../context/filter_context";
import { getUniqueValues, formatPrice } from "../utils/helpers";
import { FaCheck } from "react-icons/fa";

const Filters = () => {
  const {
    filters: {
      text,
      category,
      company,
      color,
      min_price,
      max_price,
      price,
      shipping,
    },
    updateFilters,
    clearFilters,
    all_products,
  } = useFilterContext();
  console.log("ðŸš€ ~ Filters ~ min_price", min_price);

  const categories = getUniqueValues(all_products, "category");
  const companies = getUniqueValues(all_products, "company");
  const colors = getUniqueValues(all_products, "colors");

  return (
    <Wrapper>
      <div className="content">
        <form onSubmit={(e) => e.preventDefault()}>
          {/* search */}
          <div className="form-control">
            <input
              type="text"
              name="text"
              placeholder="search"
              className="search-input"
              value={text}
              onChange={(e) => updateFilters(e)}
            />
          </div>
          {/* end of seach */}
          {/* category */}
          <div className="form-control">
            <h5>category</h5>
            <div>
              {categories.map((c, index) => {
                return (
                  <button
                    className={`${
                      category === c.toLowerCase() ? "active" : null
                    }`}
                    onClick={updateFilters}
                    type="button"
                    name="category"
                    key={index}
                  >
                    {c}
                  </button>
                );
              })}
            </div>
          </div>
          {/*end of category */}
          {/* company */}
          <div className="form-control">
            <h5>company</h5>
            <select
              className="company"
              value={company} //  value attribute specifies the initial value of the element.
              name="company"
              onChange={updateFilters}
            >
              {categories.map((c, index) => {
                return (
                  <option value={c} key={index}>
                    {c}
                  </option>
                );
              })}
            </select>
          </div>
          {/* end of company */}
          {/* color */}
          <div className="form-control">
            <h5>color</h5>
            <div className="colors">
              {colors.map((c, index) => {
                return (
                  <button
                    style={{ background: c }}
                    key={index}
                    name="color"
                    className={`${
                      color === c ? "active color-btn" : "color-btn"
                    }`}
                    data-color={c}
                    onClick={updateFilters}
                  >
                    {color === c ? <FaCheck /> : null}
                  </button>
                );
              })}
            </div>
          </div>
          {/* end of color */}
          {/* range */}
          <div className="form-control">
            <h5>price</h5>
            <p className="price">{formatPrice(price)}</p>
            <input
              type="range"
              min={min_price}
              value={price}
              name="price"
              max={max_price}
              onChange={updateFilters}
            />
          </div>
          {/* end of range */}
          {/* shiping */}
          <div className="form-control shipping">
            <label htmlFor="sipping">Free Shipping</label>
            <input
              type="checkbox"
              id="shipping"
              onChange={updateFilters}
              name="shipping"
              checked={shipping}
            />
          </div>
          {/* end of shiping */}
        </form>
      </div>
    </Wrapper>
  );
};

